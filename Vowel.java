import java.util.*;
import java.lang.*;
import java.io.*;


class Ideone

{
	public static void main (String[] args) throws java.lang.Exception
	{
		 char ch = 'e';
		 {
		     if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' )
                                    System.out.println("Vowel");
                              else if(ch>='a'&&ch<='z')
                                    System.out.println("Consonant");
	                  else
		                 System.out.println("invalid");
		 }
	}
}
	
